#include<iostream>
#include<cmath>
using namespace std;
int main(){
    // 1. Floor --->> 
    double x = 1.411;
    cout << floor(x) << endl;

    // 2. Ceil --->>
    cout << ceil(x) << endl;

    // 3. Trunc --->> 
    cout << trunc(x) << endl;

    // 4. Round --->> 
    cout << round(x) << endl;
}